CREATE TABLE cadastro_pet_adocao (
                                     id BIGINT AUTO_INCREMENT PRIMARY KEY,
                                     raca VARCHAR(100),
                                     cor VARCHAR(100),
                                     porte VARCHAR(50),
                                     estado VARCHAR(50),
                                     cidade VARCHAR(100),
                                     bairro VARCHAR(100),
                                     telefone VARCHAR(20),
                                     email VARCHAR(100)
);
